# css box model კონტეინერია, რომელიც შეიცავს მრავალ თვისებას, მათ შორის borders, margin, padding და content. ის გამოიყენება
# ვებ გვერდების დიზაინისა და განლაგების შესაქმნელად.
# borders არის საზღარი, ჩარჩო, რომელიც paddingის და contentის გარშემოა.
# margin ელემენტის ზღარსა და "მეზობელი" ელემენტების შორის არსებული სივრცე.
# padding სივრცე კონტენტისა და ელემენტის ზღვარს შორის.
# content ადგილი სადაც ნაჩვენებია ტექსტი ან რაიმე სხვა საიტის შემადგენლობა.